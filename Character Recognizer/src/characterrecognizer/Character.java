    private static double[] disruptCharacter(double[] character) {

        Random rand = new Random();
        for (int i = 0; i < 256; i++) {
            if (rand.nextInt(10) == 0) {
                character[i] = flipSquare(character[i]);
            }
        }
        return character;
    }

    //A method which flips the state of the current square
    private static double flipSquare(double squareValue) {

        if (squareValue == 0.0) {
            return 1.0;
        }
        return 0.0;
    }
